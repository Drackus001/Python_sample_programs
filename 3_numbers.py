#3 types of number
#   1. int
#   2. float
#   3. complex

i = 786
print("type(i): ",type(i)) #show type of a

f = 3.14
print("type(f): ", type(f)) #show the type of f

z = 2 - 6.1j
print("type(z): ", type(z)) #show the type of z
print("z.real: ", z.real) #complex store i in float
print("z.imag: ", z.imag) #complex store j in float
